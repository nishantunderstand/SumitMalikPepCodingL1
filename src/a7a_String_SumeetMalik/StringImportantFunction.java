package a7a_String_SumeetMalik;

public class StringImportantFunction {
	public static void main(String[] args) {

		String str = new String("FFSKHFSFSKKFSKJKJSFKFSKK");

		String strLowerString = str.toLowerCase();

		String strUpperString = str.toUpperCase();
		System.out.println(str);

		System.out.println(strLowerString);

		System.out.println(strUpperString);

		System.out.println(str);
		System.out.println();

	}
}
