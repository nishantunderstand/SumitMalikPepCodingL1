package a7a_StringSumitMalik;

public final class Student {
	private final String name;
	private final int rollNo;

	public Student(String name, int rollNo) {
		super();
		this.name = name;
		this.rollNo = rollNo;
	}

	public int getRollNo() {
		return rollNo;
	}

	public String getName() {
		return name;
	}
}
